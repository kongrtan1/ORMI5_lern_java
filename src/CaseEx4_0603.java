public class CaseEx4_0603 {

  public static void main(String[] args) {

    String color = "red";

    switch (color) {
      case "red":
        System.out.println("apple");
        break;
      case "green":
        System.out.println("Grass");
        break;
      case "blue":
        System.out.println("Sky");
        break;
    }
  }
}
