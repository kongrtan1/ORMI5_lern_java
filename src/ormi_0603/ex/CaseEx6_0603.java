package ormi_0603.ex;

public class CaseEx6_0603 {

  public static void main(String[] args) {
    String language = "Java";

    switch (language) {
      case "Java":
        System.out.println("Java는 어렵습니다.");
        break;
      case "Python":
        System.out.println("Python은 어렵습니다.");
        break;
      case "C++":
        System.out.println("C++은 어렵습니다.");
        break;
      case "JavaScript":
        System.out.println("JavaScript는 어렵습니다.");
        break;
    }
  }
}
