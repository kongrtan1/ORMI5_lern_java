public class CaseEx2_0603 {

  public static void main(String[] args) {

    String operator = "+";
    int num1 = 2;
    int num2 = 4;

    switch (operator) {
      case "+":
        System.out.println(num1 + operator + num2);
        break;
      case "-":
        System.out.println(num1 + operator + num2);
        break;
      case "*":
        System.out.println(num1 + operator + num2);
        break;
      case "/":
        System.out.println(num1 + operator + num2);
        break;
    }
  }
}
